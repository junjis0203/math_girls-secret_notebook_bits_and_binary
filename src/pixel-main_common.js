'use strict';

const bin2hex = (bin) => {
  let result = 0;
  for (let i = 0; i < 16; i++) {
    result |= (parseInt(bin[i]) << (15-i));
  }
  return result;
};

const bins2hexs = (bins) => bins.map((bin) => bin2hex(bin));

